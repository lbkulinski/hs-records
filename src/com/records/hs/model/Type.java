package com.records.hs.model;

/**
 * An entry's type in the HS Records application.
 *
 * @author Logan Kulinski, lbkulinski@icloud.com
 * @version May 12, 2020
 */
public enum Type {
    /**
     * The singleton instance representing the photo type.
     */
    PHOTO,

    /**
     * The singleton instance representing the article type.
     */
    ARTICLE,

    /**
     * The singleton instance representing the document type.
     */
    DOCUMENT,

    /**
     * The singleton instance representing the object type.
     */
    OBJECT
}