package com.records.hs.view;

/**
 * An entry's field in the HS Records application.
 *
 * @author Logan Kulinski, lbkulinski@icloud.com
 * @version May 18, 2020
 */
public enum Field {
    /**
     * The singleton instance representing the ID field.
     */
    ID,

    /**
     * The singleton instance representing the type field.
     */
    TYPE,

    /**
     * The singleton instance representing the category field.
     */
    CATEGORY,

    /**
     * The singleton instance representing the subcategory field.
     */
    SUBCATEGORY,

    /**
     * The singleton instance representing the tags field.
     */
    TAGS
}